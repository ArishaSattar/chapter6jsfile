// Step 1: Function expression
const printArgument = function (arg) {
    console.log(arg);
  };
  
  // Step 2: Pass an argument into the function expression
  printArgument("Function Expression: Hello, World!");
  
  // Step 3: Normal function declaration
  function printArgumentDeclaration(arg) {
    console.log(arg);
  }
  
  // Step 4: Pass an argument into the normal function declaration
  printArgumentDeclaration("Function Declaration: Hello, World!");
  